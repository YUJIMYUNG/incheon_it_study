package ex1_list;

public class UserInfo {
	private String id;
	private int pwd;
	
	//전체 필드에 대한 getter, stter생성 
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public int getPwd() {
		return pwd;
	}
	public void setPwd(int pwd) {
		this.pwd = pwd;
	}
	
	
}
